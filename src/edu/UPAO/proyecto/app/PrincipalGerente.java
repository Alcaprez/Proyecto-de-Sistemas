    private void verificarStockGlobal() {
        new Thread(() -> {
            try {
                edu.UPAO.proyecto.DAO.InventarioSucursalDAO inventarioDAO = new edu.UPAO.proyecto.DAO.InventarioSucursalDAO();

                // 0 = BUSCAR EN TODAS LAS SUCURSALES (VisiÃ³n de Dios/Gerente)
                java.util.List<String> alertas = inventarioDAO.obtenerAlertasBajoStock(0);

                if (!alertas.isEmpty()) {
                    StringBuilder mensaje = new StringBuilder("ðŸ“‰ REPORTE GLOBAL DE QUIEBRES DE STOCK\n");
                    mensaje.append("========================================\n\n");

                    int limite = Math.min(alertas.size(), 20); // El gerente ve mÃ¡s detalles
                    for (int i = 0; i < limite; i++) {
                        mensaje.append(alertas.get(i)).append("\n----------------------------------------\n");
                    }

                    if (alertas.size() > 20) {
                        mensaje.append("\nâš ï¸ Total de incidencias: ").append(alertas.size());
                    }

                    mensaje.append("\n\nâœ… AcciÃ³n sugerida: Contactar a proveedores.");

                    javax.swing.SwingUtilities.invokeLater(() -> {
                        javax.swing.JOptionPane.showMessageDialog(this,
                                new javax.swing.JScrollPane(new javax.swing.JTextArea(mensaje.toString(), 25, 50)),
                                "Reporte Gerencial de Abastecimiento",
                                javax.swing.JOptionPane.ERROR_MESSAGE);
                    });
                }
            } catch (Exception e) {
                System.err.println("Error stock gerente: " + e.getMessage());
            }
        }).start();
    }

