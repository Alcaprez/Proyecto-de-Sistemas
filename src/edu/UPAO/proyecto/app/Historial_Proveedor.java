    private void configurarTabla() {
        String[] columnas = {"Tipo Evento", "Resumen/Motivo", "Fecha/Hora", "Acciones"};
        modelo = new DefaultTableModel(columnas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                // Solo la 煤ltima columna (acciones) ser谩 editable
                return column == 3;
            }
        };
        jTable1.setModel(modelo);

        // Renderizador de botones
        jTable1.getColumn("Acciones").setCellRenderer((table, value, isSelected, hasFocus, row, column) -> {
            if (value != null && !value.toString().isEmpty()) {
                return new javax.swing.JButton(value.toString());
            } else {
                return new javax.swing.JLabel(""); // vac铆o si no es compra
            }
        });

        // Editor de botones
        jTable1.getColumn("Acciones").setCellEditor(new javax.swing.DefaultCellEditor(new javax.swing.JTextField()) {
            private javax.swing.JButton button = new javax.swing.JButton();
            private String value;

            {
                button.addActionListener(e -> {
                    int row = jTable1.getSelectedRow();
                    String tipo = (String) jTable1.getValueAt(row, 0);

                    if ("COMPRA".equals(tipo) || "SERVICIO".equals(tipo)) {
                        var evento = proveedorService.obtenerHistorialPorProveedor(idProveedor).get(row);

                        javax.swing.JOptionPane.showMessageDialog(jTable1,
                                "Evento: " + evento.getEvento() + "\n" +
                                "Descripci贸n: " + evento.getDescripcion() + "\n" +
                                "Fecha: " + evento.getFecha(),
                                "Detalle del evento",
                                javax.swing.JOptionPane.INFORMATION_MESSAGE
                        );
                    }
                });
            }

            @Override
            public java.awt.Component getTableCellEditorComponent(javax.swing.JTable table, Object value, boolean isSelected, int row, int column) {
                this.value = (value == null) ? "" : value.toString();
                button.setText(this.value);
                return button;
            }

            @Override
            public Object getCellEditorValue() {
                return value;
            }
        });
    }

    private void cargarHistorial() {
        modelo.setRowCount(0);

        for (var evento : proveedorService.obtenerHistorialPorProveedor(idProveedor)) {
            modelo.addRow(new Object[]{
                evento.getEvento(),
                evento.getDescripcion(),
                evento.getFecha(),
                "Ver detalle" // solo compras tienen bot贸n
            });
        }
    }

