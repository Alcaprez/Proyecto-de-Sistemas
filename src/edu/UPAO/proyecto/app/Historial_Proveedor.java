    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    String[] opciones = {"COMPRA", "SERVICIO"};
    String tipo = (String) JOptionPane.showInputDialog(
            this,
            "Seleccione tipo de evento:",
            "Nuevo Evento",
            JOptionPane.PLAIN_MESSAGE,
            null,
            opciones,
            opciones[0]);

    if (tipo == null) return; // cancelado

    String detalle = JOptionPane.showInputDialog(this, "Ingrese detalle de la " + tipo.toLowerCase() + ":");
    if (detalle == null || detalle.trim().isEmpty()) return;

    String fechaPeticionStr = JOptionPane.showInputDialog(this, "Ingrese fecha de petici贸n (YYYY-MM-DD):");
    String fechaLlegadaStr = JOptionPane.showInputDialog(this, "Ingrese fecha de llegada (YYYY-MM-DD):");
    String montoStr = JOptionPane.showInputDialog(this, "Ingrese monto pagado:");

    try {
        java.time.LocalDateTime fechaPeticion = java.time.LocalDate.parse(fechaPeticionStr).atStartOfDay();
        java.time.LocalDateTime fechaLlegada = java.time.LocalDate.parse(fechaLlegadaStr).atStartOfDay();

        //  Validar orden de fechas
        if (fechaLlegada.isBefore(fechaPeticion)) {
            JOptionPane.showMessageDialog(this,
                    "La fecha de llegada no puede ser anterior a la fecha de petici贸n.",
                    "Error de fecha",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        double monto = Double.parseDouble(montoStr);

        //  Validar monto positivo
        if (monto <= 0) {
            JOptionPane.showMessageDialog(this,
                    "El monto debe ser mayor que cero.",
                    "Error de monto",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        //  Registrar el evento usando el servicio
        proveedorService.registrarCompraOServicio(idProveedor, detalle, tipo, fechaPeticion, fechaLlegada, monto);

        JOptionPane.showMessageDialog(this, "Evento registrado correctamente.", "xito", JOptionPane.INFORMATION_MESSAGE);
        cargarHistorial();

    } catch (HeadlessException | NumberFormatException ex) {
        JOptionPane.showMessageDialog(this,
                "Error en formato de fecha o monto.\nUse el formato YYYY-MM-DD.",
                "Error",
                JOptionPane.ERROR_MESSAGE);
    }     // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

